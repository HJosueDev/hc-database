    public void buildPoputMenuTable(){
        JPopupMenu popupMenu = new JPopupMenu();
        JMenuItem menuItemAdd = new JMenuItem("Agregar Nuevo");
        JMenuItem menuItemRemove = new JMenuItem("Remover Registro");
        JMenuItem menuItemEdit = new JMenuItem("Editar Registro");
        
        
        popupMenu.add(menuItemAdd);
        popupMenu.add(menuItemRemove);
        popupMenu.add(menuItemEdit);
        
        
        menuItemAdd.addActionListener(add);
        menuItemEdit.addActionListener(edit);
        menuItemRemove.addActionListener(delete);
        
        contentTable.setComponentPopupMenu(popupMenu);
    }
        
    
