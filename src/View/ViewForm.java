    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String message="Elemento no Eliminado";
        if (createFormView.deleteData(contentTable)) {
            message="Elemento Eliminado";
            createFormView.refreshTable(contentTable);
            enabledButton(6);
        }
        JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String message="Elemento no Editado";
        if (createFormView.updateData(contentTable)) {
            message="Elemento Editado";
            createFormView.refreshTable(contentTable);
        }
        JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_btnEditActionPerformed

