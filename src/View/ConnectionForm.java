    private void btnConnectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectionActionPerformed
        // TODO add your handling code here:
        String host=txtHost.getText().trim();
        String port=txtPort.getText().trim();
        String database=txtDataBase.getText().trim();
        String user=txtUser.getText().trim();
        String password=txtPassword.getText().trim();
        
        ConnectionMysql connectionMysql=new ConnectionMysql(host, port, database, user, password);
        try {
            connection=connectionMysql.getConnection(this);
            if (connection!=null) {
                EnableOpen(true);
                cmbTables.removeAllItems();
                fillTables.fillComboboxTable(cmbTables);
            }
        } catch (SQLException ex) {
            EnableOpen(false);
            Logger.getLogger(ConnectionForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            EnableOpen(false);
            Logger.getLogger(ConnectionForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //new DataTable().setVisible(true);
        //this.dispose(); 
    }//GEN-LAST:event_btnConnectionActionPerformed

